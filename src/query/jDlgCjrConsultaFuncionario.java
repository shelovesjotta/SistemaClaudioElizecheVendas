    private void jBtnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConsultarActionPerformed
        if (jTxtNome.getText().equals("") && jTxtCpf.getText().equals("")) {
            List lista = cjrFuncionario_DAO.listAll();
            cjrFuncionarioControle.setList(lista);
        } else {
            if (!jTxtNome.getText().equals("") && !jTxtCpf.getText().equals("")) {
                List lista = cjrFuncionario_DAO.listNomeCpf(jTxtNome.getText(), jTxtCpf.getText());
                cjrFuncionarioControle.setList(lista);
            } else {
                if (!jTxtNome.getText().equals("")) {
                    List lista = cjrFuncionario_DAO.listNome(jTxtNome.getText());
                    cjrFuncionarioControle.setList(lista);
                }
                if (!jTxtCpf.getText().equals("")) {
                    List lista = cjrFuncionario_DAO.listCpf(jTxtCpf.getText());
                    cjrFuncionarioControle.setList(lista);
                }
            }
        }
    }//GEN-LAST:event_jBtnConsultarActionPerformed

